    public function countPaiementsByType(): array
    {
        return $this->createQueryBuilder('p')
            ->select('p.typePaiement AS type, COUNT(p.id) AS total')
            ->groupBy('p.typePaiement')
            ->orderBy('total', 'DESC')
            ->getQuery()
            ->getResult();
    }


